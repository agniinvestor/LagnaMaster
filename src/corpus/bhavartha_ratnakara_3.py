def _make_rules(lagna: str, data: list, start_num: int, chapter: str) -> list:
    rules = []
    num = start_num
    for entry in data:
        (planet, ptype, house_or_label, lord_of, odir, oint,
         odoms, extra_tags, vref, desc) = entry
        rid = f"BVR{num:03d}"
        if ptype == "house_placement":
            primary = {
                "planet": planet,
                "placement_type": "house_placement",
                "placement_value": [house_or_label] if isinstance(house_or_label, int) else [],
                "house": house_or_label if isinstance(house_or_label, int) else 0,
                "planet_lord_of": lord_of,
                "for_lagna": lagna,
            }
        else:
            primary = {
                "planet": planet,
                "placement_type": ptype,
                "placement_value": lord_of,
                "yoga_label": house_or_label,
                "for_lagna": lagna,
            }
        tags = ["bvr", "parashari", "bhavartha_ratnakara", lagna, planet, ptype] + extra_tags
        rules.append(RuleRecord(
            rule_id=rid,
            source="BhavarthaRatnakara",
            chapter=chapter,
            school="parashari",
            category=f"lagna_predictions_{lagna}",
            description=f"[BR — {lagna} lagna, {planet}] {desc}",
            confidence=0.65,
            tags=list(dict.fromkeys(tags)),
            implemented=False,
            primary_condition=primary,
            outcome_domains=odoms,
            outcome_direction=odir,
            outcome_intensity=oint,
            outcome_timing="dasha_dependent",
            lagna_scope=[lagna],
            verse_ref=vref,
            phase="1B_conditional",
            system="natal",
        ))
        num += 1
    return rules


def _build_all_rules() -> list:
    leo_rules = _make_rules("leo", _LEO_DATA, 261, "Ch.5")
    virgo_rules = _make_rules("virgo", _VIRGO_DATA, 261 + len(_LEO_DATA), "Ch.6")
    return leo_rules + virgo_rules


